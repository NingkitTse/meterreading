package cmcciot.onenet.nbapi.sdk.dto;

import java.util.Date;

public class MeterDeviceDto implements Cloneable {
	private String imei;
	private String deviceId;
	private String snDevice;
	private String snMeter;
	private String addressDomain;
	private String controlCode;
	private String seq;
	private Date registerDate;
	private Double freezenValue;
	private Date freezenDate;
	private String statusWord;
	private Double battery;
	private String statusValue;

	public String getImei() {
		return imei;
	}

	public void setImei(String imei) {
		this.imei = imei;
	}

	public String getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}

	public MeterDeviceDto(String imei, String deviceId) {
		super();
		this.imei = imei;
		this.deviceId = deviceId;
	}

	public boolean hasDeviceId() {
		return null != deviceId || !"".equals(deviceId);
	}

	public String getSnDevice() {
		return snDevice;
	}

	public void setSnDevice(String snDevice) {
		this.snDevice = snDevice;
	}

	public String getSnMeter() {
		return snMeter;
	}

	public void setSnMeter(String snMeter) {
		this.snMeter = snMeter;
	}

	public Date getRegisterDate() {
		return registerDate;
	}

	public void setRegisterDate(Date registerDate) {
		this.registerDate = registerDate;
	}

	public Double getFreezenValue() {
		return freezenValue;
	}

	public void setFreezenValue(Double freezenValue) {
		this.freezenValue = freezenValue;
	}

	public Date getFreezenDate() {
		return freezenDate;
	}

	public void setFreezenDate(Date freezenDate) {
		this.freezenDate = freezenDate;
	}

	public String getStatusWord() {
		return statusWord;
	}

	public void setStatusWord(String statusWord) {
		this.statusWord = statusWord;
	}

	public Double getBattery() {
		return battery;
	}

	public void setBattery(Double battery) {
		this.battery = battery;
	}

	public String getStatusValue() {
		return statusValue;
	}

	public void setStatusValue(String statusValue) {
		this.statusValue = statusValue;
	}

	public String getAddressDomain() {
		return addressDomain;
	}

	public void setAddressDomain(String addressDomain) {
		this.addressDomain = addressDomain;
	}

	public String getControlCode() {
		return controlCode;
	}

	public void setControlCode(String controlCode) {
		this.controlCode = controlCode;
	}

	public String getSeq() {
		return seq;
	}

	public void setSeq(String seq) {
		this.seq = seq;
	}

	@Override
	public Object clone() throws CloneNotSupportedException {
		return super.clone();
	}

	@Override
	public String toString() {
		return "MeterDeviceDto [imei=" + imei + ", deviceId=" + deviceId + ", snDevice=" + snDevice + ", snMeter="
				+ snMeter + ", registerDate=" + registerDate + ", freezenValue=" + freezenValue + ", freezenDate="
				+ freezenDate + ", statusWord=" + statusWord + ", battery=" + battery + ", statusValue=" + statusValue
				+ "]";
	}

}
